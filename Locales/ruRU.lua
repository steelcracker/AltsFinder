  • скорее всего у цели есть любимая зона (или набор зон)
  • для больших городов разделения запросов по расам может хватить для покрытия города
  • в наборе по умолчанию 6 запросов - это примерно 25 сек пред-скана
  • только наиболее частые расы включены в набор по умолчанию (6 из 10 рас фракции)

===|cFF00CCFF Шаг 1:|r Выбери параметр зоны
Подумай, куда часто привязывают камень возвращения в текущем дополнении. Может это одна из крупных столиц фракции. Начни с зоны, в которую цель заходит в игру.

===|cFF00CCFF Шаг 2:|r Выбери количество запросов
Цикл скана должен быть быстрее перезахода цели в игру (подробности в |cFF00FFFFнастройках|r).
Перезаход с HDD занимает полминуты, успеют выполниться 6-7 запросов. Перезаход с SSD
быстрее - 10-15 сек, а это окно всего лишь для 2-3 запросов. Если один альт цели уже
найден, замерь время перезахода цели (константа для текущей цели) - включи "время в чате"
в настройках общения.

===|cFF00CCFF Шаг 3:|r Проверь и выбери расы для сканирования
Возможно придётся периодически чередовать расы, особенно при малом кол-ве запросов.

Если не помогает, вернись на шаг 1 и выбери другую зону, их тоже стоит чередовать.]]


