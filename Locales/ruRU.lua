цикл посылает набор натсраиваемых запросов /кто. Но есть сложности:
  • результаты каждого запроса /кто ограничены сервером 50-ю игроками
  • у сервера есть кулдаун (около 5 секунд) между последовательными запросами
  • цикл скана не мгновенный: где-то 5*(#кол-во-1) сек (может цель перезаходит быстрей)
